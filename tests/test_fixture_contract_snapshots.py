def _expected_snapshot() -> dict[str, str]:
    return {
        path: digest
        for line in EXPECTED_FIXTURE_SHA256.splitlines()
        for digest, path in [line.split("  ", 1)]
    }


def _actual_snapshot() -> dict[str, str]:
    files = (
        path for root in FIXTURE_ROOTS for path in root.rglob("*") if path.is_file()
    )
    return {
        path.as_posix(): hashlib.sha256(path.read_bytes()).hexdigest()
        for path in sorted(files)
    }


def test_dataset_and_export_fixture_contract_snapshot_is_stable():
    assert _actual_snapshot() == _expected_snapshot()
